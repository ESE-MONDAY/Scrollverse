const PROJECT_VOTING_ADDRESS = '0xC1e3f6deF5403D573638aE469026eDC3906b2b96'; 

class ProjectVoting {
  constructor(web3) {
    this.contract = new web3.eth.Contract(PROJECT_VOTING_ABI, PROJECT_VOTING_ADDRESS);
  }

  async registerProject(name, description, website, account) {
    const result = await this.contract.methods.registerProject(name, description, website).send({ from: account });
    return result;
  }

  async upvote(projectId, account) {
    const result = await this.contract.methods.upvote(projectId).send({ from: account });
    return result;
  }

  async downvote(projectId, account) {
    const result = await this.contract.methods.downvote(projectId).send({ from: account });
    return result;
  }

  async donate(projectId, amount, account) {
    const result = await this.contract.methods.donate(projectId).send({ from: account, value: Web3.utils.toWei(amount, 'ether') });
    return result;
  }

  async getProject(projectId) {
    const project = await this.contract.methods.getProject(projectId).call();
    return project;
  }
  async getAllProjects() {
    const projectIds = await this.contract.methods.getAllProjects().call();
    return projectIds;
  }

  // Get details of all registered projects
  async getProjectsDetails() {
    const projects = await this.contract.methods.getProjectsDetails().call();
    return projects;
  }
}

export default ProjectVoting;
